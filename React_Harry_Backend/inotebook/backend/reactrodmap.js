// React को step-by-step सीखने के लिए एक structured learning plan होना जरूरी है। मैं आपको एक **30 दिन का React learning plan** दे रहा हूँ, जिसमें हर दिन कुछ नया और प्रैक्टिकल सीख सकते हैं। यह प्लान आपको React की मूलभूत जानकारी से लेकर उन्नत कॉन्सेप्ट्स तक ले जाएगा।

// ### Week 1: **React Basics**

// 1. **Day 1**: Introduction to React
//    - React क्या है और इसे क्यों इस्तेमाल करते हैं?
//    - React को इंस्टॉल और सेटअप करना (Create React App से प्रोजेक्ट सेटअप)।
//    - JSX और Components का परिचय।

// 2. **Day 2**: Functional Components
//    - Functional components कैसे बनाए जाते हैं।
//    - JSX सिंटैक्स और इसका उपयोग।
//    - Components में props कैसे पास करते हैं।

// 3. **Day 3**: Class Components
//    - Class-based components क्या हैं?
//    - Class components में state और lifecycle methods।
//    - Functional vs Class components का comparison।

// 4. **Day 4**: Handling Events
//    - React में event handling (onClick, onChange इत्यादि)।
//    - Events को props के माध्यम से पास करना।
//    - Event handlers में `this` को bind करना।

// 5. **Day 5**: State और Props
//    - State और props का अंतर।
//    - Components में state को manage करना।
//    - State और props के बीच data flow को समझना।

// 6. **Day 6**: Conditional Rendering
//    - React में conditional rendering कैसे किया जाता है।
//    - `if-else`, ternary operator, और logical && का उपयोग।
//    - Multiple conditional renders को manage करना।

// 7. **Day 7**: Project Day
//    - एक छोटा प्रोजेक्ट बनाएं जिसमें आप state, props, और events का उपयोग करें।
//    - जैसे एक "To-Do List" app बनाएं जिसमें tasks को add और delete किया जा सके।

// ### Week 2: **React Components और Styling**

// 8. **Day 8**: Lists और Keys
//    - React में लिस्ट्स को render करना।
//    - Keys का उपयोग और उनकी आवश्यकता।
//    - Dynamic lists के साथ काम करना।

// 9. **Day 9**: Forms और Controlled Components
//    - React में Forms को manage करना।
//    - Controlled components क्या होते हैं?
//    - Forms के साथ state को synchronize करना।

// 10. **Day 10**: Component Reusability
//     - Reusable components कैसे बनाए जाते हैं।
//     - Props का उपयोग करके components को flexible बनाना।
//     - Children props का उपयोग करना।

// 11. **Day 11**: Lifting State Up
//     - Components में data flow का प्रबंधन।
//     - State को parent component में लाना (Lifting state up)।
//     - Multiple components में shared state का उपयोग करना।

// 12. **Day 12**: Basic Styling in React
//     - Inline CSS और External CSS का उपयोग।
//     - CSS Modules और Scoped CSS का उपयोग।
//     - Styled-components का परिचय।

// 13. **Day 13**: Working with React Fragments और Portals
//     - React Fragments क्या होते हैं और उनका उपयोग।
//     - Portals का उपयोग करके DOM के बाहर components को रेंडर करना।

// 14. **Day 14**: Project Day
//     - एक प्रोजेक्ट बनाएं जिसमें Forms, Lists, और Styled components का उपयोग करें (जैसे एक Contact Form)।

// ### Week 3: **React Hooks और Advanced Concepts**

// 15. **Day 15**: Introduction to React Hooks
//     - React Hooks क्या होते हैं और क्यों उपयोग किए जाते हैं।
//     - `useState` और `useEffect` hooks का उपयोग।
//     - Functional components में state और side-effects को manage करना।

// 16. **Day 16**: More on `useEffect`
//     - `useEffect` hook के विभिन्न उपयोग।
//     - Dependency array का महत्व।
//     - Cleanup function और component unmounting को handle करना।

// 17. **Day 17**: Custom Hooks
//     - Custom hooks क्या होते हैं और उन्हें कब उपयोग किया जाता है।
//     - Code reuse के लिए custom hooks बनाना।
//     - Existing hooks का विस्तार करना।

// 18. **Day 18**: Context API
//     - Context API क्या है और इसे state management के लिए कैसे उपयोग करें।
//     - Global state बनाने के लिए Context का उपयोग।
//     - Context को multiple components में consume करना।

// 19. **Day 19**: Reducers और `useReducer` Hook
//     - `useReducer` hook का उपयोग।
//     - Reducer function कैसे काम करता है।
//     - Complex state logic के लिए `useReducer` का उपयोग।

// 20. **Day 20**: React Router Basics
//     - React Router का परिचय।
//     - Routes और Links सेट करना।
//     - Dynamic routes और URL parameters का उपयोग।

// 21. **Day 21**: Project Day
//     - एक मल्टी-पेज प्रोजेक्ट बनाएं जिसमें React Router का उपयोग हो (जैसे एक Blog application)।

// ### Week 4: **State Management और Advanced Topics**

// 22. **Day 22**: State Management with Redux (Part 1)
//     - Redux का परिचय।
//     - Redux Store, Actions, और Reducers को समझना।
//     - React-Redux लाइब्रेरी का उपयोग।

// 23. **Day 23**: State Management with Redux (Part 2)
//     - Redux के साथ state को connect करना।
//     - Complex state management के लिए Redux का उपयोग।
//     - Redux DevTools का परिचय।

// 24. **Day 24**: React Performance Optimization
//     - React में performance issues को पहचानना।
//     - React.memo, `useMemo`, और `useCallback` का उपयोग।
//     - Rendering optimizations (like Virtual DOM) समझना।

// 25. **Day 25**: Server-Side Rendering (SSR) और Next.js
//     - SSR क्या है और इसके फायदे।
//     - Next.js का परिचय।
//     - Simple Next.js app बनाना।

// 26. **Day 26**: Testing React Components
//     - Jest और React Testing Library का उपयोग करके यूनिट टेस्ट लिखना।
//     - Components के लिए टेस्ट केस लिखना।
//     - Snapshot testing का परिचय।

// 27. **Day 27**: Error Boundaries और Suspense
//     - Error boundaries का उपयोग।
//     - Suspense का उपयोग करके lazy-loaded components बनाना।

// 28. **Day 28**: Progressive Web Apps (PWAs)
//     - PWA क्या है और इसे कैसे बनाएँ।
//     - React में PWA बनाना।
//     - Service workers का उपयोग।

// 29. **Day 29**: Deployment
//     - React application को Heroku या Netlify पर deploy करना।
//     - Production build बनाना और deploy करना।

// 30. **Day 30**: Final Project
//     - एक फुल-स्टैक React प्रोजेक्ट बनाएं (जैसे एक E-commerce site या Task manager)।
//     - इसमें React Router, Redux, और Context API का उपयोग करें।
//     - इस प्रोजेक्ट को deploy करें।

